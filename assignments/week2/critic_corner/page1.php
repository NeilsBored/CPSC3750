        <h3>What makes a good movie to me?</h3>
          <li><b>Strong Character Arcs:</b> 
            Not necessarily "good" characters, but ones who evolve, who get broken down, who reveal something true about being human.</li>
          <li><b>Visual intention:</b>
            Every frame should be doing something, whether it's mood, symbolism, or just emotional texture. A great movie doesn't waste its camera.</li>
          <li><b>Sound and silence:</b> 
            Music swells, dialogue cuts, ambient noise, these things are more than background. They shape the emotional landscape.</li>
          <li><b>Theme and subtext:</b> 
